var sayHello = function(name) {
    if (name === "Jane") {
        return "Hello, Jane!";
    } else if (name === "Alex") {
        return "Hello, Alex!";
    } else if (name === "Pat") {
        return "Hello, Pat!";
    } else if (name === "World") {
        return "Hello, World!";
    } else if (name === "true") {
        return "Hello, World!";
    } else {
        return "";
    }
}
